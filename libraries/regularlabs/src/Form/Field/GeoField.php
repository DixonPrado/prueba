	protected function getListOptions($attributes)
	{
		$options = [];
		foreach ($this->{$attributes['group']} as $key => $val)
		{
			if (is_array($val))
			{
				$option              = $this->getOption($key, $key);
				$option->level       = 0;
				$option->heading     = true;
				$option->no_checkbox = true;

				$options[] = $option;

				foreach ($val as $sub_key => $sub_val)
				{
					$option        = $this->getOption($sub_key, $sub_val);
					$option->level = 1;

					$options[] = $option;
				}

				continue;
			}

			$option        = $this->getOption($key, $val);
			$option->level = 0;

			$options[] = $option;
		}

		return $options;
	}

	protected function getOption($key, $val)
	{
		if ( ! $val)
		{
			return JHtml::_('select.option', '-', '&nbsp;', 'value', 'text', true);
		}

		if ($key[0] == '-')
		{
			return JHtml::_('select.option', '-', $val, 'value', 'text', true);
		}

		$val    = RL_Form::prepareSelectItem($val);
		$option = JHtml::_('select.option', $key, $val);

		return $option;
	}

	private function cleanRegions($regions)
	{
		$regions = str_replace('"', '', $regions);

		$regions = htmlspecialchars($regions);

		// LR,MY, name for US-MD,Maryland,Maryland
		// >>
		// LR,MY,Maryland,Maryland
		// US,MD,Maryland,Maryland
		$regex = '(\n[^,]*),([^\n]*), name for ([^-]*)-([^,]*),([^\n]*\n)';
		RL_RegEx::match($regex, $regions, $match);
		while ($match)
		{
			$regions = RL_RegEx::replace($regex, '\1,\2,\5\3,\4,\5', $regions);

			RL_RegEx::match($regex, $regions, $match);
		}

		// LR,MY,Maryland,Maryland
		// >>
		// LR,MY,Maryland
		$regions = RL_RegEx::replace('(\n[^,]*,[^,]*),([^,]*),\2', '\1,\2', $regions);

		// LR,MY,District of Columbia,Disricte de Columbia
		// >>
		// LR, MY, District of Columbia, Disricte de Columbia
		$regions = str_replace(',', ', ', $regions);

		// GB, WRX, Wrexham;Wrecsam
		// >>
		// GB, WRX, Wrexham (Wrecsam)
		$regions = RL_RegEx::replace('(\n[^,]*,[^,]*,[^;]*);([^\n]*)', '\1 (\2)', $regions);

		$regions = trim(RL_RegEx::replace('  +', ' ', $regions));

		return explode("\n", $regions);
	}

	private function getRegionArray()
	{
		$regions = $this->getRegionArrayByFile();
		$regions = $this->cleanRegions($regions);

		$by_country = [];

		foreach ($regions as $full_region)
		{
			if (empty($full_region))
			{
				continue;
			}
			[$code_c, $code_r, $region] = explode(',', $full_region, 3);

			$code_c = trim($code_c);
			$code_r = trim($code_r);
			$region = trim($region);

			if ( ! isset($this->region_countries[$code_c]))
			{
				continue;
			}

			$country = $this->countries[$code_c];

			if ( ! isset($by_country[$code_c]))
			{
				$by_country[$code_c] = [];
			}

			$name = $this->getRegionName($country, $region);

			$by_country[$code_c][$region] = '\'' . $code_c . '-' . $code_r . '\'  => "' . $name . '",';
		}

		$min_regions = 3;

		$output = [];
		foreach ($this->region_countries as $code_c => $country)
		{
			if ( ! isset($by_country[$code_c]))
			{
				continue;
			}

			$regions = $by_country[$code_c];

			if (count($regions) < $min_regions)
			{
				continue;
			}

			ksort($regions);

			if (empty($output))
			{
				$output[] = '\'-' . $code_c . '\'  => "' . $country . '",';
				$output[] = implode("\n", $regions);

				continue;
			}

			$output[] = '';
			$output[] = '\'--' . $code_c . '\'  => "", \'-' . $code_c . '\'  => "' . $country . '",';
			$output[] = implode("\n", $regions);
		}

		echo implode("\n", $output);

		exit;
	}

	private function getRegionArrayByFile()
	{
		// https://www.softcatala.org/wiki/ISO_3166_2
		return trim(file_get_contents(__DIR__ . '/geo.iso.regions.txt'));
	}

	private function getRegionName($country, $region)
	{
		switch ($country . ': ' . $region)
		{
			case 'Mexico: Distrito Federal':
				return 'Mexico: Distrito Federal (Mexico City)';

			default:
				return $country . ': ' . $region;
		}
	}
